package datatype;

public class ArithmeticOperators {
public static void main(String[]args) {
	int num1 = 10;
	int num2 = 5;
	int add = num1 + num2;
	int sub = num1-num2;
	int multiply = num1*num2;
	int division = num1/num2;
	int modulus= num1%num2;
	System.out.println(" Addition of 10 and 5 = "+add);
	System.out.println(" Difference between 10 and 5 = "+sub);
	System.out.println(" Multiplication of 10 and 5 = "+multiply);
	System.out.println(" Division quotient of 10 and 5 ="+division);
	System.out.println(" Modulus of 10 and 5 = "+modulus);
}
}
